import docx
from docx.shared import Inches, Pt, RGBColor
from docx.enum.text import WD_ALIGN_PARAGRAPH

def create_document():
    doc = docx.Document()
    
    # Page setup
    for section in doc.sections:
        section.top_margin = Inches(1)
        section.bottom_margin = Inches(1)
        section.left_margin = Inches(1)
        section.right_margin = Inches(1)

    # Style setup
    styles = doc.styles
    normal_style = styles['Normal']
    normal_style.font.name = 'Arial'
    normal_style.font.size = Pt(11)
    normal_style.font.color.rgb = RGBColor(0x22, 0x22, 0x22)
    
    # Title
    title = doc.add_paragraph()
    title_run = title.add_run("Документация продукта: «Блокнот Айгерим»")
    title_run.font.name = 'Arial'
    title_run.font.size = Pt(22)
    title_run.font.bold = True
    title_run.font.color.rgb = RGBColor(0x1F, 0x2D, 0x3D)
    title.alignment = WD_ALIGN_PARAGRAPH.CENTER
    
    # Subtitle
    subtitle = doc.add_paragraph()
    sub_run = subtitle.add_run("The Bulletproof Daily Journal (Цифровой ежедневный блокнот)")
    sub_run.font.size = Pt(14)
    sub_run.font.italic = True
    sub_run.font.color.rgb = RGBColor(0x55, 0x55, 0x55)
    subtitle.alignment = WD_ALIGN_PARAGRAPH.CENTER
    
    doc.add_paragraph() # spacer
    
    # Section helper
    def add_heading_1(text):
        h = doc.add_paragraph()
        run = h.add_run(text)
        run.font.size = Pt(16)
        run.font.bold = True
        run.font.color.rgb = RGBColor(0x1F, 0x2D, 0x3D)
        h.paragraph_format.space_before = Pt(18)
        h.paragraph_format.space_after = Pt(6)
        return h

    def add_heading_2(text):
        h = doc.add_paragraph()
        run = h.add_run(text)
        run.font.size = Pt(13)
        run.font.bold = True
        run.font.color.rgb = RGBColor(0x2E, 0x40, 0x53)
        h.paragraph_format.space_before = Pt(12)
        h.paragraph_format.space_after = Pt(4)
        return h
        
    def add_body(text, bold=False, italic=False):
        p = doc.add_paragraph()
        run = p.add_run(text)
        run.font.size = Pt(11)
        run.bold = bold
        run.italic = italic
        p.paragraph_format.space_after = Pt(6)
        return p

    def add_bullet(text):
        p = doc.add_paragraph(style='List Bullet')
        run = p.add_run(text)
        run.font.size = Pt(11)
        p.paragraph_format.space_after = Pt(4)
        return p

    # --- 1. Проблема и пользователь ---
    add_heading_1("1. Проблема и пользователь")
    
    add_heading_2("В чём настоящая боль Айгерим?")
    add_body("Айгерим ведет салон на 3 мастеров с помощью бумажного журнала. Настоящая причина, почему она до сих пор использует бумагу, а не сложные CRM — скорость ввода и когнитивная простота. Запись клиента на бумаге занимает 3 секунды: «черкнуть имя в нужную строчку». Сложные CRM требуют кучу кликов, авторизаций, выбора категорий и настроек, на которые у занятой и уставшей Айгерим нет ресурса.")
    
    add_body("Однако бумажный журнал имеет критические недостатки:")
    add_bullet("Double-Bookings (Накладки): Записывая клиентов на бегу или по телефону, Айгерим случайно пишет два имени на одно и то же время или забывает, что «Сложное окрашивание» занимает 3 часа, и записывает другого человека на следующий час к тому же мастеру.")
    add_bullet("No-Shows (Неявки): Клиенты забывают о визитах. Отсутствие автоматических напоминаний стоит Айгерим живых денег (до 20% потерянной выручки из-за пустых окон).")
    add_bullet("Хаос при отменах: Зачеркивания в блокноте создают визуальный шум, из-за чего Айгерим не видит свободные окна, которые можно быстро перепродать.")
    
    add_heading_2("Пользователи и их роли")
    add_bullet("Айгерим (Администратор/Владелица): Главный оператор системы. Её цель — быстро распределять входящие заявки по мастерам, видеть загрузку на сегодня/завтра и контролировать подтверждения визитов.")
    add_bullet("Мастера (Аида, Бэлла, Клара): Получатели информации. Им нужно видеть чистое расписание без накладок на свой рабочий день.")
    add_bullet("Клиенты: Пользователи онлайн-записи и получатели уведомлений. Их цель — быстро выбрать удобное время и подтвердить свой визит в 1 клик.")
    
    add_heading_2("Главная работа продукта (JTBD)")
    add_body("Когда я на ходу управляю записями в салоне, я хочу быстро фиксировать клиентов в расписании с автоматической защитой от накладок и быстрой сверкой подтверждений, чтобы избавиться от стресса двойных бронирований, сократить неявки до нуля и не тратить время на администрирование сложных систем.", italic=True)

    # --- 2. Скоуп и решение (v1) ---
    add_heading_1("2. Скоуп и решение (v1)")
    
    add_heading_2("Что мы строим в v1 (Now)")
    add_body("Мы строим «Цифровой аналог бумажного листа» — предельно простой ежедневный дашборд:")
    add_bullet("Интервальный контроль накладок: Система автоматически «растягивает» записи по часовой сетке на основе длительности услуги и не даёт совершить случайную накладку (показывая предупреждение и список накладывающихся клиентов).")
    add_bullet("WhatsApp-сверка в 1 клик: Генерация ссылки с готовым шаблоном сообщения для отправки клиенту.")
    add_bullet("Личный кабинет клиента: Минималистичная веб-страница, где клиент видит детали своей записи и может нажать «Да, я приду», что мгновенно меняет статус записи в блокноте Айгерим на зеленый.")
    add_bullet("Подсветка освободившихся окон: При отмене записи слот на 5 секунд загорается зеленым («Освободилось!»), позволяя Айгерим быстро сориентироваться.")
    
    add_heading_2("Что мы НЕ строим в v1")
    add_bullet("Авторизацию по паролям/SMS для Айгерим и клиентов (используется сессионный доступ по прямым ссылкам).")
    add_bullet("Модуль склада и расходных материалов.")
    add_bullet("Расчет зарплат мастеров и финансовую аналитику.")
    add_bullet("Интеграцию с онлайн-кассами.")
    
    add_heading_2("Допущения и компромиссы")
    add_bullet("Сетка времени: В v1 используется фиксированная сетка шагом в 30 минут / 1 час для простоты визуализации и быстроты выбора.")
    add_bullet("WhatsApp-интеграция: Вместо дорогой интеграции через официальный WhatsApp Business API, мы используем wa.me ссылки, которые открывают приложение WhatsApp на телефоне Айгерим с уже заполненным текстом.")

    # --- 3. Требования и User Flow ---
    add_heading_1("3. Требования и User Flow")
    
    add_heading_2("Функциональные требования")
    add_bullet("Календарь на 3 дня: Просмотр расписания на Сегодня, Завтра и Послезавтра.")
    add_bullet("Селектор специалистов: Вкладки для мгновенного переключения между 3 мастерами.")
    add_bullet("Форма быстрой записи: Поля: Мастер, Дата, Время начала, Длительность (30м, 1ч, 1.5ч, 2ч, 3ч), Имя клиента, Телефон, Услуга.")
    add_bullet("Контроль пересечений: Проверка пересечений по времени и вывод предупреждения о наложении.")
    add_bullet("WhatsApp-уведомление: Генерация шаблона сообщения с деталями записи.")
    add_bullet("Подтверждение от клиента: Интерактивная кнопка подтверждения в портале клиента с обратной связью в админ-панель.")
    
    add_heading_2("Основной User Flow")
    add_bullet("Айгерим добавляет запись клиента на определенное время с указанием длительности (например, 1.5 часа).")
    add_bullet("Система проверяет наличие конфликтов с уже существующими записями на выбранный интервал.")
    add_bullet("Если конфликтов нет, запись сохраняется в статусе «Ожидает».")
    add_bullet("Айгерим нажимает «Напомнить», что генерирует сообщение для отправки клиенту в WhatsApp.")
    add_bullet("Клиент переходит по ссылке, открывает личный кабинет и подтверждает визит.")
    add_bullet("Запись в календаре Айгерим автоматически становится зеленой («Подтверждено клиентом»).")

    # --- 4. Нарезка на релизы и модули ---
    add_heading_1("4. Нарезка на релизы и модули")
    
    # Add Table
    table = doc.add_table(rows=1, cols=4)
    table.style = 'Light Shading Accent 1'
    hdr_cells = table.rows[0].cells
    hdr_cells[0].text = 'Модуль'
    hdr_cells[1].text = 'v1 (Now) - Прототип'
    hdr_cells[2].text = 'v2 (Next) - MVP'
    hdr_cells[3].text = 'v3 (Later) - Развитие'
    
    modules_data = [
      ('Расписание', 'Календарь на 3 дня, сетка 1 час, карточки продолжения услуг', 'Календарь на месяц, сетка с шагом 15 минут', 'Автоматическая оптимизация окон (AI)'),
      ('Конфликты', 'Предупреждение в форме, ручной оверрайд, stacked-карточки', 'Блокировка наложения по выбору админа', 'Статистика накладок и перегрузки мастеров'),
      ('Уведомления', 'Ручная симуляция WhatsApp-шаблона', 'Автоматическая рассылка через WhatsApp Web', 'Голосовой робот-обзвон для подтверждений'),
      ('Клиентский портал', 'Онлайн-запись на свободные окна, подтверждение визита', 'Личный кабинет со всеми записями', 'Программа лояльности (бонусы)')
    ]
    
    for mod, v1, v2, v3 in modules_data:
        row_cells = table.add_row().cells
        row_cells[0].text = mod
        row_cells[1].text = v1
        row_cells[2].text = v2
        row_cells[3].text = v3
        
    doc.add_paragraph() # spacer

    add_heading_2("Логика нарезки: почему это в v1?")
    add_body("v1 фокусируется исключительно на защите от критических ошибок (накладки) и ликвидации неявок (сверка по WhatsApp). Эти функции дают 80% ценности продукта. База данных на месяц и аналитика не имеют смысла, если Айгерим продолжает страдать от того, что два клиента приходят на стрижку в 10:00 к одному мастеру.")

    # --- 5. Прототип одного флоу ---
    add_heading_1("5. Прототип одного флоу")
    add_body("Выбранный флоу: «Полный цикл подтверждения записи и контроль наложений»")
    add_bullet("Айгерим видит наложение записей (Амина и Алия на 10:00).")
    add_bullet("Айгерим отправляет напоминание Мадине на 11:00 (статус «Ожидает»).")
    add_bullet("Клиент Мадина на своем экране подтверждает запись.")
    add_bullet("В блокноте Айгерим статус записи Мадины автоматически меняется на «Подтверждено».")
    add_bullet("При отмене записи слот загорается зеленым «Освободилось!».")
    
    add_heading_2("Why was this flow chosen?")
    add_body("Этот флоу демонстрирует сердце продукта — двухстороннее бесшовное общение салона и клиента без участия сложных систем. Он решает обе главные боли (накладки и неявки) в рамках одного экрана и показывает, насколько цифровой блокнот удобнее и чище бумажного.")

    # --- 6. Отчёт о разработке ---
    add_heading_1("6. Отчёт о разработке")
    add_heading_2("На что ушло время")
    add_bullet("Разработка математической модели расчета пересечений интервалов времени с плавающей точкой (Time-to-Float) для адаптивной сетки расписания.")
    add_bullet("Проектирование side-by-side интерфейса, чтобы тестировщик мог видеть реакцию мастера и клиента одновременно на одном экране без перезагрузок.")
    add_bullet("Написание логики 'зеленого коридора' (недавно освободившихся окон при удалении визита) и функции Undo (Вернуть запись) в Toast-уведомлениях.")
    
    add_heading_2("Что бы я сделал, будь у меня еще 3 дня")
    add_bullet("Интегрировал бы тестовый шлюз рассылок (например, Twilio или GreenAPI), чтобы отправлять реальные SMS или WhatsApp-сообщения.")
    add_bullet("Добавил бы автозаполнение клиентов по первым буквам имени или телефона.")
    add_bullet("Реализовал бы драг-н-дроп (перетаскивание карточек записей с часа на час или между мастерами).")
    
    doc.save("/Users/alenpak/Desktop/task/Product_Documentation.docx")
    print("Document created successfully!")

if __name__ == "__main__":
    create_document()
